const DICTS: Record<Lang, Dict> = { zh, en, ko, ar, vi, ms }

function format(str: string, vars?: Record<string, string | number>) {
  if (!vars) return str
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`))
}

type I18nCtx = {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string, vars?: Record<string, string | number>) => string
}

const Ctx = createContext<I18nCtx | null>(null)

const LANG_KEY = 'dinoai_crm_lang'

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem(LANG_KEY) as Lang | null
    return saved && DICTS[saved] ? saved : 'zh'
  })

  const setLang = useCallback((l: Lang) => {
    setLangState(l)
    localStorage.setItem(LANG_KEY, l)
    dayjs.locale(DAYJS_LOCALE[l])
    document.documentElement.lang = l
    document.documentElement.dir = RTL_LANGS.includes(l) ? 'rtl' : 'ltr'
  }, [])

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = DICTS[lang]
      const str = dict[key] ?? en[key] ?? key
      return format(str, vars)
    },
    [lang],
  )

  // 初始化方向 / dayjs
  useMemo(() => {
    dayjs.locale(DAYJS_LOCALE[lang])
    document.documentElement.lang = lang
    document.documentElement.dir = RTL_LANGS.includes(lang) ? 'rtl' : 'ltr'
  }, [lang])

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t])
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>
}

export function useI18n() {
  const ctx = useContext(Ctx)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
